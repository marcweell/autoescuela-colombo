    #indexes
    public function index(Request $request)
    {
        try {
            $statement_patrimony_item = $this->statement_patrimony_itemServiceQuery->findAll();
            $view = view('user.fragments.statement_patrimony_item.listForm', [
                'statement_patrimony_item' => $statement_patrimony_item
