class useristrative_actController extends Controller
{
    private $useristrative_actService;
    private $useristrative_actServiceQuery;
    function __construct()
    {
        $this->useristrative_actService = new useristrative_actServiceImpl();
        $this->useristrative_actServiceQuery = new useristrative_actServiceQueryImpl();
    }
    public function add(Request $request)
    {
        $data = new stdClass();
        foreach ($request->all() as $key => $value) {
            $data->{$key} = $value;
        }
        $data->code = code(null,__METHOD__);
        try {
            $this->useristrative_actService->add($data);
            return (new WebApi())->setSuccess()->notify(__("OperaciÃ³n realizada con Ã©xito"))->resync()->close_modal()->get();
        } catch (\Exception $e) {
