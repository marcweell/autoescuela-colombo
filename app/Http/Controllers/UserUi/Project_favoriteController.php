class Project_favoriteController extends Controller
{
    private $project_favoriteService;
    private $project_favoriteServiceQuery;
    function __construct()
    {
        $this->project_favoriteService = new Project_favoriteServiceImpl();
        $this->project_favoriteServiceQuery = new Project_favoriteServiceQueryImpl();
    }
